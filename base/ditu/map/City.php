        }

        if (array_search($district, self::$_cityMap[$city]) === NULL) {
            return FALSE;
        }

        return TRUE;
    }

    public static function provinceCityDistrictsTree($filter = null)
    {
        $provinceCitys = self::getOpenCitysGroupbyProvince(false);
        $data = [];
        foreach ($provinceCitys as $province => $citys){
            if ($filter && !isset($filter[$province])){
                continue;
            }

            $options = [];
            foreach ($citys as $item){
                if ($filter && !empty($filter[$province]) && !isset($filter[$province][$item[0]])){
                    continue;
                }

                $city = [
                    'value' => $item[0],
                    'label' => $item[0],
                    'children' => []
                ];

                $districts = self::getDistrictByCity($item[0]);
                foreach ($districts as $district){
                    if ($filter && !empty($filter[$province]) && !empty($filter[$province][$item[0]]) &&
                        !in_array($district, $filter[$province][$item[0]])){
                        continue;
                    }

                    $city['children'][] = [
                        'value' => $district,
                        'label' => $district
                    ];
                }

                $options[] = $city;
            }

            $data[] = [
                'value' => $province,
                'label' => $province,
                'children' => $options
            ];
        }

        return $data;
    }

